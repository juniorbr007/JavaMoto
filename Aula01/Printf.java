public class Printf {
	
	public static void main(String[] args) {
		 System.out.printf("hello world\n");
		 System.out.printf("valor inteiro: %d %d\n", 10, 20);
		 System.out.printf("valor real: %f.\n", 3.14159265);
		 System.out.printf("valor real com apenas duas casas: %.2f.\n", 3.14159265);
		 System.out.printf("dado de texto: %c.\n", 'a');
		 System.out.printf("dado de texto: %s.\n", "linguagem Java");
	}

}